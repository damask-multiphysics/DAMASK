""" ----------------------------------------------------------------------------

    *** COMPARE DAMASK RESULTS TO ANALYTICAL SOLUTION ***

    Compare the results from DAMASK to a reference (exact/analytical) solution
    for a few simple, linear elastic cases.

    Parametrization:
     - Polytopes:
       - 2D: triangles (tri) and quadrilaterals (quad)
       - 3D: tetrahedra (tet) and hexahedra (hex)
     - Elasticity properties (xtropy)
       - Isotropic
       - Anisotropic
     - Boundary conditions
        - Where it is applied (BC_point)
           - Node and edge loads (2D)
           - Node and face (surface) loads (3D)
        - Type of BC (BC_type)
           - Displacements (u_dot)
           - Concentrated/point forces (f_dot)

    Meshes:
     - 2D:
       - Triangles: square in [0,10]^2
          - 5 nodes (4 external, 1 internal)
          - 4 elements
       - Quadrilaterals: square in [0,10]^2
          - 9 nodes (4 external, 1 internal, 4 mid-edge)
          - 4 elements
     - 3D:
       - Tetrahedra: cube in [0,1]^3
         - 10 nodes (8 external, 2 mid-face)
         - 12 elements
       - Hexahedra: cube in [0,10]^3
         - 16 nodes (8 external, 8 internal)
         - 7 elements

    Randomization:
     - Elastic constants (C_11, C_12, ...)
     - Orientations (in the material.yaml file)
     - Magnitude of the displacement/force BC
     - DOF on which BC are applied

    The exact (analytical) displacement solution is computed using the
    exact stiffness matrices (as a function of elastic properties).
    Also provided analytical solutions for (small) strains, true (Cauchy)
    stresses, first Piola-Kirchhoff stresses and deformation gradients.

    Resources (in resources/mesh_analytical):

    - Load (-l)
      - load.yaml
    - Material (-m)
      - material_base.yaml (without orientations)
    - Mesh (-g)
      - mesh_{polytope}.msh
    - Numerics configuration (-n)
      - numerics.yaml
    - Additional files:
      - Node coordinates: mesh_{polytope}_coords.dat

    ------------------------------------------------------------------------ """

