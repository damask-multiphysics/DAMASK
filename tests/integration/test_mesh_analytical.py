def node_coords(res_path, polytope):
    """Read node coordinates."""
    return np.loadtxt(res_path/f'mesh_{polytope}_coords.dat')


def connectivity(polytope, cell = None):
    """Mesh connectivity."""
    if polytope == 'tri':
        conn = np.array([
            [0, 1, 4],
            [1, 2, 4],
            [2, 3, 4],
            [3, 0, 4]
        ])
    elif polytope == 'quad':
        conn = np.array([
            [0, 5, 4, 8],
            [5, 1, 6, 4],
            [6, 2, 7, 4],
            [7, 3, 8, 4]
        ])
    elif polytope == 'tet':
        conn = np.array([
            [3, 2, 8, 1],
            [9, 1, 0, 3],
            [1, 4, 9, 0],
            [9, 1, 3, 8],
            [9, 5, 1, 8],
            [5, 9, 1, 4],
            [2, 6, 3, 8],
            [9, 3, 6, 8],
            [9, 6, 5, 8],
            [6, 9, 5, 7],
            [9, 5, 7, 4],
            [9, 3, 7, 6]
        ])
    elif polytope == 'hex':
        conn = np.array([
            [0,  1,  2,  3,  8,  9, 10, 11],
            [5,  1,  0,  4, 13,  9,  8, 12],
            [6,  2,  1,  5, 14, 10,  9, 13],
            [7,  3,  2,  6, 15, 11, 10, 14],
            [4,  0,  3,  7, 12,  8, 11, 15],
            [8,  9, 10, 11, 12, 13, 14, 15],
            [6,  5,  4,  7, 14, 13, 12, 15]
        ])

    return conn if cell == None else conn[cell]


def J_mat(polytope, x, y, z = None, qPoint = None):
    """Jacobian matrix."""
    if polytope == 'tri':
        J = np.array([
                [x[1]-x[0], y[1]-y[0]],
                [x[2]-x[0], y[2]-y[0]],
            ]) / 2.0
    elif polytope == 'quad':
        n_D = 2
        data = quadrature(n_D, qPoint)
        r = data[1]
        s = data[2]

        J = np.empty([n_D, n_D])
        J[0,0] = ((s+1)*(x[2]-x[3])+(s-1)*(x[0]-x[1]))/4
        J[0,1] = ((s+1)*(y[2]-y[3])+(s-1)*(y[0]-y[1]))/4
        J[1,0] = ((r+1)*(x[2]-x[1])+(r-1)*(x[0]-x[3]))/4
        J[1,1] = ((r+1)*(y[2]-y[1])+(r-1)*(y[0]-y[3]))/4

    elif polytope == 'tet':
        J = np.array([
                [x[1]-x[0], y[1]-y[0], z[1]-z[0]],
                [x[2]-x[0], y[2]-y[0], z[2]-z[0]],
                [x[3]-x[0], y[3]-y[0], z[3]-z[0]]
            ]) / 2.0
    else:
        n_D = 3
        data = quadrature(n_D, qPoint)
        r = data[1]
        s = data[2]
        t = data[3]

        J = np.empty([n_D, n_D])
        J[0,0] = -(((s+1)*t+s+1)*x[7]+((-s-1)*t-s-1)*x[6]+((s-1)*t+s-1)*x[5]+((1-s)*t-s+1)*x[4]+ \
                  ((-s-1)*t+s+1)*x[3]+((s+1)*t-s-1)*x[2]+((1-s)*t+s-1)*x[1]+((s-1)*t-s+1)*x[0])/8
        J[0,1] = -(((s+1)*t+s+1)*y[7]+((-s-1)*t-s-1)*y[6]+((s-1)*t+s-1)*y[5]+((1-s)*t-s+1)*y[4]+ \
                  ((-s-1)*t+s+1)*y[3]+((s+1)*t-s-1)*y[2]+((1-s)*t+s-1)*y[1]+((s-1)*t-s+1)*y[0])/8
        J[0,2] = -(((s+1)*t+s+1)*z[7]+((-s-1)*t-s-1)*z[6]+((s-1)*t+s-1)*z[5]+((1-s)*t-s+1)*z[4]+ \
                  ((-s-1)*t+s+1)*z[3]+((s+1)*t-s-1)*z[2]+((1-s)*t+s-1)*z[1]+((s-1)*t-s+1)*z[0])/8

        J[1,0] = -(((r-1)*t+r-1)*x[7]+((-r-1)*t-r-1)*x[6]+((r+1)*t+r+1)*x[5]+((1-r)*t-r+1)*x[4]+ \
                  ((1-r)*t+r-1)*x[3]+((r+1)*t-r-1)*x[2]+((-r-1)*t+r+1)*x[1]+((r-1)*t-r+1)*x[0])/8
        J[1,1] = -(((r-1)*t+r-1)*y[7]+((-r-1)*t-r-1)*y[6]+((r+1)*t+r+1)*y[5]+((1-r)*t-r+1)*y[4]+ \
                  ((1-r)*t+r-1)*y[3]+((r+1)*t-r-1)*y[2]+((-r-1)*t+r+1)*y[1]+((r-1)*t-r+1)*y[0])/8
